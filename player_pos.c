#include "so_long.h"

t_data	player_pos(char **c)
{
	t_data	k;
	int		i;
	int		j;

	i = 0;
	while (c[i])
	{
		j = 0;
		while (c[i][j])
		{
			if (c[i][j] == 'P')
			{
				k.x = j;
				k.y = i;
			}
			j++;
		}
		i++;
	}
	return (k);
}
